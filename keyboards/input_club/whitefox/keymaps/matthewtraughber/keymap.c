// https://www.reddit.com/r/olkb/comments/swuy68/tap_both_shift_keys_for_caps_lock_works_with/
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if ((get_mods() & MOD_MASK_SHIFT) == MOD_MASK_SHIFT) { // https://docs.qmk.fm/#/feature_advanced_keycodes?id=checking-modifier-state
        tap_code(KC_CAPS);
    }

    return true;
};

